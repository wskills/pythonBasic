#  tries to find the greater value in the list
# The concept is rather simple - we temporarily assume that the first element is the largest one, 
# and check the hypothesis against all the remaining elements in the list.

# Example 1
my_list = [17, 3, 11, 5, 1, 9, 7, 15, 13]
largest = my_list[0]

for i in range(1, len(my_list)):
    if my_list[i] > largest:
        largest = my_list[i]

print(largest)

# Example 2

my_list = [17, 3, 11, 5, 1, 9, 7, 15, 13]
largest = my_list[0]

for i in my_list[1:]:
    if i > largest:
        largest = i

print(largest)

